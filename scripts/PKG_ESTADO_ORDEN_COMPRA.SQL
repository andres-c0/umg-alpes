CREATE OR REPLACE PACKAGE PKG_ESTADO_ORDEN_COMPRA AS

  PROCEDURE SP_INSERTAR_ESTADO_ORDEN_COMPRA(
    P_CODIGO       IN ESTADO_ORDEN_COMPRA.CODIGO%TYPE,
    P_DESCRIPCION  IN ESTADO_ORDEN_COMPRA.DESCRIPCION%TYPE,
    P_ESTADO_OC_ID OUT ESTADO_ORDEN_COMPRA.ESTADO_OC_ID%TYPE
  );

  PROCEDURE SP_ACTUALIZAR_ESTADO_ORDEN_COMPRA(
    P_ESTADO_OC_ID IN ESTADO_ORDEN_COMPRA.ESTADO_OC_ID%TYPE,
    P_CODIGO       IN ESTADO_ORDEN_COMPRA.CODIGO%TYPE,
    P_DESCRIPCION  IN ESTADO_ORDEN_COMPRA.DESCRIPCION%TYPE
  );

  PROCEDURE SP_ELIMINAR_ESTADO_ORDEN_COMPRA(
    P_ESTADO_OC_ID IN ESTADO_ORDEN_COMPRA.ESTADO_OC_ID%TYPE
  );

  PROCEDURE SP_OBTENER_ESTADO_ORDEN_COMPRA(
    P_ESTADO_OC_ID IN  ESTADO_ORDEN_COMPRA.ESTADO_OC_ID%TYPE,
    P_CURSOR       OUT SYS_REFCURSOR
  );

  PROCEDURE SP_LISTAR_ESTADOS_ORDEN_COMPRA(
    P_CURSOR OUT SYS_REFCURSOR
  );

  PROCEDURE SP_BUSCAR_ESTADOS_ORDEN_COMPRA(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  );

END PKG_ESTADO_ORDEN_COMPRA;
/

CREATE OR REPLACE PACKAGE BODY PKG_ESTADO_ORDEN_COMPRA AS

  PROCEDURE SP_INSERTAR_ESTADO_ORDEN_COMPRA(
    P_CODIGO       IN ESTADO_ORDEN_COMPRA.CODIGO%TYPE,
    P_DESCRIPCION  IN ESTADO_ORDEN_COMPRA.DESCRIPCION%TYPE,
    P_ESTADO_OC_ID OUT ESTADO_ORDEN_COMPRA.ESTADO_OC_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO ESTADO_ORDEN_COMPRA (CODIGO, DESCRIPCION, CREATED_AT, ESTADO)
    VALUES (P_CODIGO, P_DESCRIPCION, SYSTIMESTAMP, 'ACTIVO')
    RETURNING ESTADO_OC_ID INTO P_ESTADO_OC_ID;
  END SP_INSERTAR_ESTADO_ORDEN_COMPRA;

  PROCEDURE SP_ACTUALIZAR_ESTADO_ORDEN_COMPRA(
    P_ESTADO_OC_ID IN ESTADO_ORDEN_COMPRA.ESTADO_OC_ID%TYPE,
    P_CODIGO       IN ESTADO_ORDEN_COMPRA.CODIGO%TYPE,
    P_DESCRIPCION  IN ESTADO_ORDEN_COMPRA.DESCRIPCION%TYPE
  ) AS
  BEGIN
    UPDATE ESTADO_ORDEN_COMPRA
    SET CODIGO      = P_CODIGO,
        DESCRIPCION = P_DESCRIPCION,
        UPDATED_AT  = SYSTIMESTAMP
    WHERE ESTADO_OC_ID = P_ESTADO_OC_ID;
  END SP_ACTUALIZAR_ESTADO_ORDEN_COMPRA;

  PROCEDURE SP_ELIMINAR_ESTADO_ORDEN_COMPRA(
    P_ESTADO_OC_ID IN ESTADO_ORDEN_COMPRA.ESTADO_OC_ID%TYPE
  ) AS
    V_EXISTE NUMBER;
  BEGIN
    SELECT COUNT(*) INTO V_EXISTE
    FROM ORDEN_COMPRA
    WHERE ESTADO_OC_ID = P_ESTADO_OC_ID;

    IF V_EXISTE > 0 THEN
      RAISE_APPLICATION_ERROR(-20001, 'No se puede eliminar el estado porque esta asignado a ordenes de compra.');
    END IF;

    UPDATE ESTADO_ORDEN_COMPRA
    SET ESTADO = 'INACTIVO',
        UPDATED_AT = SYSTIMESTAMP
    WHERE ESTADO_OC_ID = P_ESTADO_OC_ID;
  END SP_ELIMINAR_ESTADO_ORDEN_COMPRA;

  PROCEDURE SP_OBTENER_ESTADO_ORDEN_COMPRA(
    P_ESTADO_OC_ID IN  ESTADO_ORDEN_COMPRA.ESTADO_OC_ID%TYPE,
    P_CURSOR       OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT ESTADO_OC_ID,
             CODIGO,
             DESCRIPCION,
             CREATED_AT,
             UPDATED_AT,
             ESTADO
      FROM ESTADO_ORDEN_COMPRA
      WHERE ESTADO_OC_ID = P_ESTADO_OC_ID;
  END SP_OBTENER_ESTADO_ORDEN_COMPRA;

  PROCEDURE SP_LISTAR_ESTADOS_ORDEN_COMPRA(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT ESTADO_OC_ID,
             CODIGO,
             DESCRIPCION,
             CREATED_AT,
             UPDATED_AT,
             ESTADO
      FROM ESTADO_ORDEN_COMPRA
      WHERE ESTADO = 'ACTIVO'
      ORDER BY CODIGO;
  END SP_LISTAR_ESTADOS_ORDEN_COMPRA;

  PROCEDURE SP_BUSCAR_ESTADOS_ORDEN_COMPRA(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT ESTADO_OC_ID,
             CODIGO,
             DESCRIPCION,
             CREATED_AT,
             UPDATED_AT,
             ESTADO
      FROM ESTADO_ORDEN_COMPRA
      WHERE ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'CODIGO'      AND UPPER(CODIGO)      LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'DESCRIPCION' AND UPPER(DESCRIPCION) LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY CODIGO;
  END SP_BUSCAR_ESTADOS_ORDEN_COMPRA;

END PKG_ESTADO_ORDEN_COMPRA;
