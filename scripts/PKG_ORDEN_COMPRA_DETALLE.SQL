CREATE OR REPLACE PACKAGE BODY PKG_ORDEN_COMPRA_DETALLE AS

  PROCEDURE SP_INSERTAR_ORDEN_COMPRA_DETALLE(
    P_ORDEN_COMPRA_ID      IN ORDEN_COMPRA_DETALLE.ORDEN_COMPRA_ID%TYPE,
    P_MP_ID                IN ORDEN_COMPRA_DETALLE.MP_ID%TYPE,
    P_CANTIDAD             IN ORDEN_COMPRA_DETALLE.CANTIDAD%TYPE,
    P_COSTO_UNITARIO       IN ORDEN_COMPRA_DETALLE.COSTO_UNITARIO%TYPE,
    P_SUBTOTAL_LINEA       IN ORDEN_COMPRA_DETALLE.SUBTOTAL_LINEA%TYPE,
    P_ORDEN_COMPRA_DET_ID  OUT ORDEN_COMPRA_DETALLE.ORDEN_COMPRA_DET_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO ORDEN_COMPRA_DETALLE (
      ORDEN_COMPRA_ID, MP_ID, CANTIDAD, COSTO_UNITARIO, SUBTOTAL_LINEA, CREATED_AT, ESTADO
    ) VALUES (
      P_ORDEN_COMPRA_ID, P_MP_ID, P_CANTIDAD, P_COSTO_UNITARIO, P_SUBTOTAL_LINEA, SYSTIMESTAMP, 'ACTIVO'
    )
    RETURNING ORDEN_COMPRA_DET_ID INTO P_ORDEN_COMPRA_DET_ID;
  END SP_INSERTAR_ORDEN_COMPRA_DETALLE;

  PROCEDURE SP_ACTUALIZAR_ORDEN_COMPRA_DETALLE(
    P_ORDEN_COMPRA_DET_ID  IN ORDEN_COMPRA_DETALLE.ORDEN_COMPRA_DET_ID%TYPE,
    P_ORDEN_COMPRA_ID      IN ORDEN_COMPRA_DETALLE.ORDEN_COMPRA_ID%TYPE,
    P_MP_ID                IN ORDEN_COMPRA_DETALLE.MP_ID%TYPE,
    P_CANTIDAD             IN ORDEN_COMPRA_DETALLE.CANTIDAD%TYPE,
    P_COSTO_UNITARIO       IN ORDEN_COMPRA_DETALLE.COSTO_UNITARIO%TYPE,
    P_SUBTOTAL_LINEA       IN ORDEN_COMPRA_DETALLE.SUBTOTAL_LINEA%TYPE
  ) AS
  BEGIN
    UPDATE ORDEN_COMPRA_DETALLE
    SET ORDEN_COMPRA_ID = P_ORDEN_COMPRA_ID,
        MP_ID           = P_MP_ID,
        CANTIDAD        = P_CANTIDAD,
        COSTO_UNITARIO  = P_COSTO_UNITARIO,
        SUBTOTAL_LINEA  = P_SUBTOTAL_LINEA,
        UPDATED_AT      = SYSTIMESTAMP
    WHERE ORDEN_COMPRA_DET_ID = P_ORDEN_COMPRA_DET_ID;
  END SP_ACTUALIZAR_ORDEN_COMPRA_DETALLE;

  PROCEDURE SP_ELIMINAR_ORDEN_COMPRA_DETALLE(
    P_ORDEN_COMPRA_DET_ID IN ORDEN_COMPRA_DETALLE.ORDEN_COMPRA_DET_ID%TYPE
  ) AS
  BEGIN
    UPDATE ORDEN_COMPRA_DETALLE
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE ORDEN_COMPRA_DET_ID = P_ORDEN_COMPRA_DET_ID;
  END SP_ELIMINAR_ORDEN_COMPRA_DETALLE;

  PROCEDURE SP_OBTENER_ORDEN_COMPRA_DETALLE(
    P_ORDEN_COMPRA_DET_ID IN  ORDEN_COMPRA_DETALLE.ORDEN_COMPRA_DET_ID%TYPE,
    P_CURSOR              OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT OCD.ORDEN_COMPRA_DET_ID, OCD.ORDEN_COMPRA_ID, OCD.MP_ID,
             MP.NOMBRE AS MATERIA_PRIMA_NOMBRE,
             OCD.CANTIDAD, OCD.COSTO_UNITARIO, OCD.SUBTOTAL_LINEA, OCD.ESTADO
      FROM ORDEN_COMPRA_DETALLE OCD
      JOIN MATERIA_PRIMA MP ON MP.MP_ID = OCD.MP_ID
      WHERE OCD.ORDEN_COMPRA_DET_ID = P_ORDEN_COMPRA_DET_ID;
  END SP_OBTENER_ORDEN_COMPRA_DETALLE;

  PROCEDURE SP_LISTAR_ORDEN_COMPRA_DETALLE(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT OCD.ORDEN_COMPRA_DET_ID, OCD.ORDEN_COMPRA_ID, OCD.MP_ID,
             MP.NOMBRE AS MATERIA_PRIMA_NOMBRE,
             OCD.CANTIDAD, OCD.COSTO_UNITARIO, OCD.SUBTOTAL_LINEA, OCD.ESTADO
      FROM ORDEN_COMPRA_DETALLE OCD
      JOIN MATERIA_PRIMA MP ON MP.MP_ID = OCD.MP_ID
      WHERE OCD.ESTADO = 'ACTIVO'
      ORDER BY OCD.ORDEN_COMPRA_ID, OCD.ORDEN_COMPRA_DET_ID;
  END SP_LISTAR_ORDEN_COMPRA_DETALLE;

  PROCEDURE SP_BUSCAR_ORDEN_COMPRA_DETALLE(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT OCD.ORDEN_COMPRA_DET_ID, OCD.ORDEN_COMPRA_ID, OCD.MP_ID,
             MP.NOMBRE AS MATERIA_PRIMA_NOMBRE,
             OCD.CANTIDAD, OCD.COSTO_UNITARIO, OCD.SUBTOTAL_LINEA, OCD.ESTADO
      FROM ORDEN_COMPRA_DETALLE OCD
      JOIN MATERIA_PRIMA MP ON MP.MP_ID = OCD.MP_ID
      WHERE OCD.ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'ORDENCOMPRA'   AND TO_CHAR(OCD.ORDEN_COMPRA_ID) LIKE '%' || P_VALOR || '%')
          OR
          (UPPER(P_CRITERIO) = 'MATERIAPRIMA'  AND UPPER(MP.NOMBRE) LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY OCD.ORDEN_COMPRA_ID, OCD.ORDEN_COMPRA_DET_ID;
  END SP_BUSCAR_ORDEN_COMPRA_DETALLE;

END PKG_ORDEN_COMPRA_DETALLE;
