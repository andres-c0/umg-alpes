  CONSTRAINT ck_inventario_producto_stock CHECK (stock >= 0 AND stock_reservado >= 0),
  CONSTRAINT ck_inventario_producto_estado CHECK (estado IN ('ACTIVO','INACTIVO'))
);

CREATE TABLE Movimiento_Inventario (
