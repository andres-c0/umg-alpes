------------------------------------------------------------
-- 1) SEGURIDAD
------------------------------------------------------------
CREATE TABLE Rol (
  rol_id        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  rol_nombre    VARCHAR2(60) NOT NULL,
  descripcion   VARCHAR2(200),
  created_at    TIMESTAMP NOT NULL,
  updated_at    TIMESTAMP,
  estado        VARCHAR2(20),
  CONSTRAINT uq_rol_nombre UNIQUE (rol_nombre),
  CONSTRAINT ck_rol_estado CHECK (estado IN ('ACTIVO','INACTIVO'))
);

CREATE TABLE Permiso (
  permiso_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo        VARCHAR2(80) NOT NULL,
  descripcion   VARCHAR2(200) NOT NULL,
  modulo        VARCHAR2(60),
  created_at    TIMESTAMP NOT NULL,
  updated_at    TIMESTAMP,
  estado        VARCHAR2(20),
  CONSTRAINT uq_permiso_codigo UNIQUE (codigo),
  CONSTRAINT ck_permiso_estado CHECK (estado IN ('ACTIVO','INACTIVO'))
);

CREATE TABLE Rol_Permiso (
  rol_permiso_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  rol_id         NUMBER NOT NULL,
  permiso_id     NUMBER NOT NULL,
  es_permitido   NUMBER(1) DEFAULT 1 NOT NULL,
  created_at     TIMESTAMP NOT NULL,
  updated_at     TIMESTAMP,
  estado         VARCHAR2(20),
  CONSTRAINT fk_rol_permiso_rol FOREIGN KEY (rol_id) REFERENCES Rol(rol_id),
  CONSTRAINT fk_rol_permiso_permiso FOREIGN KEY (permiso_id) REFERENCES Permiso(permiso_id),
  CONSTRAINT ck_rol_permiso_permitido CHECK (es_permitido IN (0,1)),
  CONSTRAINT ck_rol_permiso_estado CHECK (estado IN ('ACTIVO','INACTIVO'))
);

