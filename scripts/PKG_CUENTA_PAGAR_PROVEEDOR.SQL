CREATE OR REPLACE PACKAGE BODY PKG_CUENTA_PAGAR_PROVEEDOR AS

  PROCEDURE SP_INSERTAR_CUENTA_PAGAR_PROVEEDOR(
    P_PROV_ID           IN CUENTA_PAGAR_PROVEEDOR.PROV_ID%TYPE,
    P_ORDEN_COMPRA_ID   IN CUENTA_PAGAR_PROVEEDOR.ORDEN_COMPRA_ID%TYPE,
    P_SALDO             IN CUENTA_PAGAR_PROVEEDOR.SALDO%TYPE,
    P_FECHA_VENCIMIENTO IN CUENTA_PAGAR_PROVEEDOR.FECHA_VENCIMIENTO%TYPE,
    P_ESTADO_CP         IN CUENTA_PAGAR_PROVEEDOR.ESTADO_CP%TYPE,
    P_CUENTA_PAGAR_ID   OUT CUENTA_PAGAR_PROVEEDOR.CUENTA_PAGAR_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO CUENTA_PAGAR_PROVEEDOR (
      PROV_ID, ORDEN_COMPRA_ID, SALDO, FECHA_VENCIMIENTO, ESTADO_CP, CREATED_AT, ESTADO
    ) VALUES (
      P_PROV_ID, P_ORDEN_COMPRA_ID, P_SALDO, P_FECHA_VENCIMIENTO, P_ESTADO_CP, SYSTIMESTAMP, 'ACTIVO'
    )
    RETURNING CUENTA_PAGAR_ID INTO P_CUENTA_PAGAR_ID;
  END SP_INSERTAR_CUENTA_PAGAR_PROVEEDOR;

  PROCEDURE SP_ACTUALIZAR_CUENTA_PAGAR_PROVEEDOR(
    P_CUENTA_PAGAR_ID   IN CUENTA_PAGAR_PROVEEDOR.CUENTA_PAGAR_ID%TYPE,
    P_PROV_ID           IN CUENTA_PAGAR_PROVEEDOR.PROV_ID%TYPE,
    P_ORDEN_COMPRA_ID   IN CUENTA_PAGAR_PROVEEDOR.ORDEN_COMPRA_ID%TYPE,
    P_SALDO             IN CUENTA_PAGAR_PROVEEDOR.SALDO%TYPE,
    P_FECHA_VENCIMIENTO IN CUENTA_PAGAR_PROVEEDOR.FECHA_VENCIMIENTO%TYPE,
    P_ESTADO_CP         IN CUENTA_PAGAR_PROVEEDOR.ESTADO_CP%TYPE
  ) AS
  BEGIN
    UPDATE CUENTA_PAGAR_PROVEEDOR
    SET PROV_ID           = P_PROV_ID,
        ORDEN_COMPRA_ID   = P_ORDEN_COMPRA_ID,
        SALDO             = P_SALDO,
        FECHA_VENCIMIENTO = P_FECHA_VENCIMIENTO,
        ESTADO_CP         = P_ESTADO_CP,
        UPDATED_AT        = SYSTIMESTAMP
    WHERE CUENTA_PAGAR_ID = P_CUENTA_PAGAR_ID;
  END SP_ACTUALIZAR_CUENTA_PAGAR_PROVEEDOR;

  PROCEDURE SP_ELIMINAR_CUENTA_PAGAR_PROVEEDOR(
    P_CUENTA_PAGAR_ID IN CUENTA_PAGAR_PROVEEDOR.CUENTA_PAGAR_ID%TYPE
  ) AS
    V_EXISTE NUMBER;
  BEGIN
    SELECT COUNT(*) INTO V_EXISTE
    FROM PAGO_PROVEEDOR
    WHERE CUENTA_PAGAR_ID = P_CUENTA_PAGAR_ID;

    IF V_EXISTE > 0 THEN
      RAISE_APPLICATION_ERROR(-20001, 'No se puede eliminar la cuenta por pagar porque ya tiene pagos registrados.');
    END IF;

    UPDATE CUENTA_PAGAR_PROVEEDOR
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE CUENTA_PAGAR_ID = P_CUENTA_PAGAR_ID;
  END SP_ELIMINAR_CUENTA_PAGAR_PROVEEDOR;

  PROCEDURE SP_OBTENER_CUENTA_PAGAR_PROVEEDOR(
    P_CUENTA_PAGAR_ID IN  CUENTA_PAGAR_PROVEEDOR.CUENTA_PAGAR_ID%TYPE,
    P_CURSOR          OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CPP.CUENTA_PAGAR_ID, CPP.PROV_ID, P.RAZON_SOCIAL,
             CPP.ORDEN_COMPRA_ID, OC.NUM_OC,
             CPP.SALDO, CPP.FECHA_VENCIMIENTO, CPP.ESTADO_CP, CPP.ESTADO
      FROM CUENTA_PAGAR_PROVEEDOR CPP
      JOIN PROVEEDOR P    ON P.PROV_ID          = CPP.PROV_ID
      JOIN ORDEN_COMPRA OC ON OC.ORDEN_COMPRA_ID = CPP.ORDEN_COMPRA_ID
      WHERE CPP.CUENTA_PAGAR_ID = P_CUENTA_PAGAR_ID;
  END SP_OBTENER_CUENTA_PAGAR_PROVEEDOR;

  PROCEDURE SP_LISTAR_CUENTAS_PAGAR_PROVEEDOR(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CPP.CUENTA_PAGAR_ID, P.RAZON_SOCIAL, OC.NUM_OC,
             CPP.SALDO, CPP.FECHA_VENCIMIENTO, CPP.ESTADO_CP, CPP.ESTADO
      FROM CUENTA_PAGAR_PROVEEDOR CPP
      JOIN PROVEEDOR P    ON P.PROV_ID          = CPP.PROV_ID
      JOIN ORDEN_COMPRA OC ON OC.ORDEN_COMPRA_ID = CPP.ORDEN_COMPRA_ID
      WHERE CPP.ESTADO = 'ACTIVO'
      ORDER BY CPP.FECHA_VENCIMIENTO;
  END SP_LISTAR_CUENTAS_PAGAR_PROVEEDOR;

  PROCEDURE SP_BUSCAR_CUENTAS_PAGAR_PROVEEDOR(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CPP.CUENTA_PAGAR_ID, P.RAZON_SOCIAL, OC.NUM_OC,
             CPP.SALDO, CPP.FECHA_VENCIMIENTO, CPP.ESTADO_CP, CPP.ESTADO
      FROM CUENTA_PAGAR_PROVEEDOR CPP
      JOIN PROVEEDOR P    ON P.PROV_ID          = CPP.PROV_ID
      JOIN ORDEN_COMPRA OC ON OC.ORDEN_COMPRA_ID = CPP.ORDEN_COMPRA_ID
      WHERE CPP.ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'PROVEEDOR' AND UPPER(P.RAZON_SOCIAL) LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'NUMOC'     AND UPPER(OC.NUM_OC)      LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'ESTADOCP'  AND UPPER(CPP.ESTADO_CP)  LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY CPP.FECHA_VENCIMIENTO;
  END SP_BUSCAR_CUENTAS_PAGAR_PROVEEDOR;

END PKG_CUENTA_PAGAR_PROVEEDOR;
