CREATE OR REPLACE PACKAGE BODY PKG_EXPEDIENTE_PROVEEDOR AS

  PROCEDURE SP_INSERTAR_EXPEDIENTE_PROVEEDOR(
    P_PROV_ID                  IN EXPEDIENTE_PROVEEDOR.PROV_ID%TYPE,
    P_TIPO_DOCUMENTO           IN EXPEDIENTE_PROVEEDOR.TIPO_DOCUMENTO%TYPE,
    P_URL_DOCUMENTO            IN EXPEDIENTE_PROVEEDOR.URL_DOCUMENTO%TYPE,
    P_FECHA_DOCUMENTO          IN EXPEDIENTE_PROVEEDOR.FECHA_DOCUMENTO%TYPE,
    P_EXPEDIENTE_PROVEEDOR_ID  OUT EXPEDIENTE_PROVEEDOR.EXPEDIENTE_PROVEEDOR_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO EXPEDIENTE_PROVEEDOR (
      PROV_ID, TIPO_DOCUMENTO, URL_DOCUMENTO, FECHA_DOCUMENTO, CREATED_AT, ESTADO
    ) VALUES (
      P_PROV_ID, P_TIPO_DOCUMENTO, P_URL_DOCUMENTO, P_FECHA_DOCUMENTO, SYSTIMESTAMP, 'ACTIVO'
    )
    RETURNING EXPEDIENTE_PROVEEDOR_ID INTO P_EXPEDIENTE_PROVEEDOR_ID;
  END SP_INSERTAR_EXPEDIENTE_PROVEEDOR;

  PROCEDURE SP_ACTUALIZAR_EXPEDIENTE_PROVEEDOR(
    P_EXPEDIENTE_PROVEEDOR_ID  IN EXPEDIENTE_PROVEEDOR.EXPEDIENTE_PROVEEDOR_ID%TYPE,
    P_PROV_ID                  IN EXPEDIENTE_PROVEEDOR.PROV_ID%TYPE,
    P_TIPO_DOCUMENTO           IN EXPEDIENTE_PROVEEDOR.TIPO_DOCUMENTO%TYPE,
    P_URL_DOCUMENTO            IN EXPEDIENTE_PROVEEDOR.URL_DOCUMENTO%TYPE,
    P_FECHA_DOCUMENTO          IN EXPEDIENTE_PROVEEDOR.FECHA_DOCUMENTO%TYPE
  ) AS
  BEGIN
    UPDATE EXPEDIENTE_PROVEEDOR
    SET PROV_ID         = P_PROV_ID,
        TIPO_DOCUMENTO  = P_TIPO_DOCUMENTO,
        URL_DOCUMENTO   = P_URL_DOCUMENTO,
        FECHA_DOCUMENTO = P_FECHA_DOCUMENTO,
        UPDATED_AT      = SYSTIMESTAMP
    WHERE EXPEDIENTE_PROVEEDOR_ID = P_EXPEDIENTE_PROVEEDOR_ID;
  END SP_ACTUALIZAR_EXPEDIENTE_PROVEEDOR;

  PROCEDURE SP_ELIMINAR_EXPEDIENTE_PROVEEDOR(
    P_EXPEDIENTE_PROVEEDOR_ID IN EXPEDIENTE_PROVEEDOR.EXPEDIENTE_PROVEEDOR_ID%TYPE
  ) AS
  BEGIN
    UPDATE EXPEDIENTE_PROVEEDOR
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE EXPEDIENTE_PROVEEDOR_ID = P_EXPEDIENTE_PROVEEDOR_ID;
  END SP_ELIMINAR_EXPEDIENTE_PROVEEDOR;

  PROCEDURE SP_OBTENER_EXPEDIENTE_PROVEEDOR(
    P_EXPEDIENTE_PROVEEDOR_ID IN  EXPEDIENTE_PROVEEDOR.EXPEDIENTE_PROVEEDOR_ID%TYPE,
    P_CURSOR                  OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT EP.EXPEDIENTE_PROVEEDOR_ID, EP.PROV_ID, P.RAZON_SOCIAL,
             EP.TIPO_DOCUMENTO, EP.URL_DOCUMENTO, EP.FECHA_DOCUMENTO, EP.ESTADO
      FROM EXPEDIENTE_PROVEEDOR EP
      JOIN PROVEEDOR P ON P.PROV_ID = EP.PROV_ID
      WHERE EP.EXPEDIENTE_PROVEEDOR_ID = P_EXPEDIENTE_PROVEEDOR_ID;
  END SP_OBTENER_EXPEDIENTE_PROVEEDOR;

  PROCEDURE SP_LISTAR_EXPEDIENTES_PROVEEDOR(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT EP.EXPEDIENTE_PROVEEDOR_ID, P.RAZON_SOCIAL,
             EP.TIPO_DOCUMENTO, EP.FECHA_DOCUMENTO, EP.ESTADO
      FROM EXPEDIENTE_PROVEEDOR EP
      JOIN PROVEEDOR P ON P.PROV_ID = EP.PROV_ID
      WHERE EP.ESTADO = 'ACTIVO'
      ORDER BY P.RAZON_SOCIAL, EP.TIPO_DOCUMENTO;
  END SP_LISTAR_EXPEDIENTES_PROVEEDOR;

  PROCEDURE SP_BUSCAR_EXPEDIENTES_PROVEEDOR(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT EP.EXPEDIENTE_PROVEEDOR_ID, P.RAZON_SOCIAL,
             EP.TIPO_DOCUMENTO, EP.FECHA_DOCUMENTO, EP.ESTADO
      FROM EXPEDIENTE_PROVEEDOR EP
      JOIN PROVEEDOR P ON P.PROV_ID = EP.PROV_ID
      WHERE EP.ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'PROVEEDOR'      AND UPPER(P.RAZON_SOCIAL)  LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'TIPODOCUMENTO'  AND UPPER(EP.TIPO_DOCUMENTO) LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY P.RAZON_SOCIAL, EP.TIPO_DOCUMENTO;
  END SP_BUSCAR_EXPEDIENTES_PROVEEDOR;

END PKG_EXPEDIENTE_PROVEEDOR;
/
