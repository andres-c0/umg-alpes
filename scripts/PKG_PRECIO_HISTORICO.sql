    P_MOTIVO           IN  PRECIO_HISTORICO.MOTIVO%TYPE,
    P_PRECIO_HIST_ID   OUT PRECIO_HISTORICO.PRECIO_HIST_ID%TYPE
  );

  PROCEDURE SP_ACTUALIZAR_PRECIO_HISTORICO(
    P_PRECIO_HIST_ID   IN  PRECIO_HISTORICO.PRECIO_HIST_ID%TYPE,
    P_PRODUCTO_ID      IN  PRECIO_HISTORICO.PRODUCTO_ID%TYPE,
