  );

  PROCEDURE SP_ELIMINAR_TIPO_PROMOCION(
    P_TIPO_PROMOCION_ID IN TIPO_PROMOCION.TIPO_PROMOCION_ID%TYPE
  );

  PROCEDURE SP_OBTENER_TIPO_PROMOCION(
    P_TIPO_PROMOCION_ID IN TIPO_PROMOCION.TIPO_PROMOCION_ID%TYPE,
    P_CURSOR            OUT SYS_REFCURSOR
