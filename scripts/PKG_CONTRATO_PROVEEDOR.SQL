  PROCEDURE SP_BUSCAR_CONTRATOS_PROVEEDOR(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  );

END PKG_CONTRATO_PROVEEDOR;
/

CREATE OR REPLACE PACKAGE BODY PKG_CONTRATO_PROVEEDOR AS

  PROCEDURE SP_INSERTAR_CONTRATO_PROVEEDOR(
    P_PROV_ID               IN CONTRATO_PROVEEDOR.PROV_ID%TYPE,
    P_TITULO                IN CONTRATO_PROVEEDOR.TITULO%TYPE,
    P_VIGENCIA_INICIO       IN CONTRATO_PROVEEDOR.VIGENCIA_INICIO%TYPE,
    P_VIGENCIA_FIN          IN CONTRATO_PROVEEDOR.VIGENCIA_FIN%TYPE,
    P_URL_DOCUMENTO         IN CONTRATO_PROVEEDOR.URL_DOCUMENTO%TYPE,
    P_CONTRATO_PROVEEDOR_ID OUT CONTRATO_PROVEEDOR.CONTRATO_PROVEEDOR_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO CONTRATO_PROVEEDOR (
      PROV_ID, TITULO, VIGENCIA_INICIO, VIGENCIA_FIN, URL_DOCUMENTO, CREATED_AT, ESTADO
    ) VALUES (
      P_PROV_ID, P_TITULO, P_VIGENCIA_INICIO, P_VIGENCIA_FIN, P_URL_DOCUMENTO, SYSTIMESTAMP, 'ACTIVO'
    )
    RETURNING CONTRATO_PROVEEDOR_ID INTO P_CONTRATO_PROVEEDOR_ID;
  END SP_INSERTAR_CONTRATO_PROVEEDOR;

  PROCEDURE SP_ACTUALIZAR_CONTRATO_PROVEEDOR(
    P_CONTRATO_PROVEEDOR_ID IN CONTRATO_PROVEEDOR.CONTRATO_PROVEEDOR_ID%TYPE,
    P_PROV_ID               IN CONTRATO_PROVEEDOR.PROV_ID%TYPE,
    P_TITULO                IN CONTRATO_PROVEEDOR.TITULO%TYPE,
    P_VIGENCIA_INICIO       IN CONTRATO_PROVEEDOR.VIGENCIA_INICIO%TYPE,
    P_VIGENCIA_FIN          IN CONTRATO_PROVEEDOR.VIGENCIA_FIN%TYPE,
    P_URL_DOCUMENTO         IN CONTRATO_PROVEEDOR.URL_DOCUMENTO%TYPE
  ) AS
  BEGIN
    UPDATE CONTRATO_PROVEEDOR
    SET PROV_ID         = P_PROV_ID,
        TITULO          = P_TITULO,
        VIGENCIA_INICIO = P_VIGENCIA_INICIO,
        VIGENCIA_FIN    = P_VIGENCIA_FIN,
        URL_DOCUMENTO   = P_URL_DOCUMENTO,
        UPDATED_AT      = SYSTIMESTAMP
    WHERE CONTRATO_PROVEEDOR_ID = P_CONTRATO_PROVEEDOR_ID;
  END SP_ACTUALIZAR_CONTRATO_PROVEEDOR;

  PROCEDURE SP_ELIMINAR_CONTRATO_PROVEEDOR(
    P_CONTRATO_PROVEEDOR_ID IN CONTRATO_PROVEEDOR.CONTRATO_PROVEEDOR_ID%TYPE
  ) AS
  BEGIN
    UPDATE CONTRATO_PROVEEDOR
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE CONTRATO_PROVEEDOR_ID = P_CONTRATO_PROVEEDOR_ID;
  END SP_ELIMINAR_CONTRATO_PROVEEDOR;

  PROCEDURE SP_OBTENER_CONTRATO_PROVEEDOR(
    P_CONTRATO_PROVEEDOR_ID IN  CONTRATO_PROVEEDOR.CONTRATO_PROVEEDOR_ID%TYPE,
    P_CURSOR                OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CP.CONTRATO_PROVEEDOR_ID, CP.PROV_ID, P.RAZON_SOCIAL,
             CP.TITULO, CP.VIGENCIA_INICIO, CP.VIGENCIA_FIN,
             CP.URL_DOCUMENTO, CP.ESTADO
      FROM CONTRATO_PROVEEDOR CP
      JOIN PROVEEDOR P ON P.PROV_ID = CP.PROV_ID
      WHERE CP.CONTRATO_PROVEEDOR_ID = P_CONTRATO_PROVEEDOR_ID;
  END SP_OBTENER_CONTRATO_PROVEEDOR;

  PROCEDURE SP_LISTAR_CONTRATOS_PROVEEDOR(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CP.CONTRATO_PROVEEDOR_ID, P.RAZON_SOCIAL, CP.TITULO,
             CP.VIGENCIA_INICIO, CP.VIGENCIA_FIN, CP.ESTADO
      FROM CONTRATO_PROVEEDOR CP
      JOIN PROVEEDOR P ON P.PROV_ID = CP.PROV_ID
      WHERE CP.ESTADO = 'ACTIVO'
      ORDER BY P.RAZON_SOCIAL, CP.TITULO;
  END SP_LISTAR_CONTRATOS_PROVEEDOR;

  PROCEDURE SP_BUSCAR_CONTRATOS_PROVEEDOR(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CP.CONTRATO_PROVEEDOR_ID, P.RAZON_SOCIAL, CP.TITULO,
             CP.VIGENCIA_INICIO, CP.VIGENCIA_FIN, CP.ESTADO
      FROM CONTRATO_PROVEEDOR CP
      JOIN PROVEEDOR P ON P.PROV_ID = CP.PROV_ID
      WHERE CP.ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'PROVEEDOR' AND UPPER(P.RAZON_SOCIAL) LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'TITULO'    AND UPPER(CP.TITULO)      LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY P.RAZON_SOCIAL, CP.TITULO;
  END SP_BUSCAR_CONTRATOS_PROVEEDOR;

END PKG_CONTRATO_PROVEEDOR;
