    P_TIPO          IN  VEHICULO.TIPO%TYPE,
    P_CAPACIDAD_KG  IN  VEHICULO.CAPACIDAD_KG%TYPE,
    P_CAPACIDAD_M3  IN  VEHICULO.CAPACIDAD_M3%TYPE,
