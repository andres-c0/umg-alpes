CREATE OR REPLACE PACKAGE BODY PKG_PAGO_PROVEEDOR AS

  PROCEDURE SP_INSERTAR_PAGO_PROVEEDOR(
    P_CUENTA_PAGAR_ID   IN PAGO_PROVEEDOR.CUENTA_PAGAR_ID%TYPE,
    P_MONTO             IN PAGO_PROVEEDOR.MONTO%TYPE,
    P_FECHA_PAGO        IN PAGO_PROVEEDOR.FECHA_PAGO%TYPE,
    P_REFERENCIA        IN PAGO_PROVEEDOR.REFERENCIA%TYPE,
    P_PAGO_PROVEEDOR_ID OUT PAGO_PROVEEDOR.PAGO_PROVEEDOR_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO PAGO_PROVEEDOR (CUENTA_PAGAR_ID, MONTO, FECHA_PAGO, REFERENCIA, CREATED_AT, ESTADO)
    VALUES (P_CUENTA_PAGAR_ID, P_MONTO, P_FECHA_PAGO, P_REFERENCIA, SYSTIMESTAMP, 'ACTIVO')
    RETURNING PAGO_PROVEEDOR_ID INTO P_PAGO_PROVEEDOR_ID;
  END SP_INSERTAR_PAGO_PROVEEDOR;

  PROCEDURE SP_ACTUALIZAR_PAGO_PROVEEDOR(
    P_PAGO_PROVEEDOR_ID IN PAGO_PROVEEDOR.PAGO_PROVEEDOR_ID%TYPE,
    P_CUENTA_PAGAR_ID   IN PAGO_PROVEEDOR.CUENTA_PAGAR_ID%TYPE,
    P_MONTO             IN PAGO_PROVEEDOR.MONTO%TYPE,
    P_FECHA_PAGO        IN PAGO_PROVEEDOR.FECHA_PAGO%TYPE,
    P_REFERENCIA        IN PAGO_PROVEEDOR.REFERENCIA%TYPE
  ) AS
  BEGIN
    UPDATE PAGO_PROVEEDOR
    SET CUENTA_PAGAR_ID = P_CUENTA_PAGAR_ID,
        MONTO           = P_MONTO,
        FECHA_PAGO      = P_FECHA_PAGO,
        REFERENCIA      = P_REFERENCIA,
        UPDATED_AT      = SYSTIMESTAMP
    WHERE PAGO_PROVEEDOR_ID = P_PAGO_PROVEEDOR_ID;
  END SP_ACTUALIZAR_PAGO_PROVEEDOR;

  PROCEDURE SP_ELIMINAR_PAGO_PROVEEDOR(
    P_PAGO_PROVEEDOR_ID IN PAGO_PROVEEDOR.PAGO_PROVEEDOR_ID%TYPE
  ) AS
  BEGIN
    UPDATE PAGO_PROVEEDOR
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE PAGO_PROVEEDOR_ID = P_PAGO_PROVEEDOR_ID;
  END SP_ELIMINAR_PAGO_PROVEEDOR;

  PROCEDURE SP_OBTENER_PAGO_PROVEEDOR(
    P_PAGO_PROVEEDOR_ID IN  PAGO_PROVEEDOR.PAGO_PROVEEDOR_ID%TYPE,
    P_CURSOR            OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT PP.PAGO_PROVEEDOR_ID, PP.CUENTA_PAGAR_ID,
             PRV.RAZON_SOCIAL, PP.MONTO, PP.FECHA_PAGO, PP.REFERENCIA, PP.ESTADO
      FROM PAGO_PROVEEDOR PP
      JOIN CUENTA_PAGAR_PROVEEDOR CPP ON CPP.CUENTA_PAGAR_ID = PP.CUENTA_PAGAR_ID
      JOIN PROVEEDOR PRV              ON PRV.PROV_ID          = CPP.PROV_ID
      WHERE PP.PAGO_PROVEEDOR_ID = P_PAGO_PROVEEDOR_ID;
  END SP_OBTENER_PAGO_PROVEEDOR;

  PROCEDURE SP_LISTAR_PAGOS_PROVEEDOR(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT PP.PAGO_PROVEEDOR_ID, PRV.RAZON_SOCIAL,
             PP.MONTO, PP.FECHA_PAGO, PP.REFERENCIA, PP.ESTADO
      FROM PAGO_PROVEEDOR PP
      JOIN CUENTA_PAGAR_PROVEEDOR CPP ON CPP.CUENTA_PAGAR_ID = PP.CUENTA_PAGAR_ID
      JOIN PROVEEDOR PRV              ON PRV.PROV_ID          = CPP.PROV_ID
      WHERE PP.ESTADO = 'ACTIVO'
      ORDER BY PP.FECHA_PAGO DESC;
  END SP_LISTAR_PAGOS_PROVEEDOR;

  PROCEDURE SP_BUSCAR_PAGOS_PROVEEDOR(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT PP.PAGO_PROVEEDOR_ID, PRV.RAZON_SOCIAL,
             PP.MONTO, PP.FECHA_PAGO, PP.REFERENCIA, PP.ESTADO
      FROM PAGO_PROVEEDOR PP
      JOIN CUENTA_PAGAR_PROVEEDOR CPP ON CPP.CUENTA_PAGAR_ID = PP.CUENTA_PAGAR_ID
      JOIN PROVEEDOR PRV              ON PRV.PROV_ID          = CPP.PROV_ID
      WHERE PP.ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'PROVEEDOR'  AND UPPER(PRV.RAZON_SOCIAL) LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'REFERENCIA' AND UPPER(PP.REFERENCIA)    LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY PP.FECHA_PAGO DESC;
  END SP_BUSCAR_PAGOS_PROVEEDOR;

END PKG_PAGO_PROVEEDOR;
