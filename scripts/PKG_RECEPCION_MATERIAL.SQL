CREATE OR REPLACE PACKAGE BODY PKG_RECEPCION_MATERIAL AS

  PROCEDURE SP_INSERTAR_RECEPCION_MATERIAL(
    P_ORDEN_COMPRA_ID        IN RECEPCION_MATERIAL.ORDEN_COMPRA_ID%TYPE,
    P_FECHA_RECEPCION        IN RECEPCION_MATERIAL.FECHA_RECEPCION%TYPE,
    P_EMP_ID_RECIBE          IN RECEPCION_MATERIAL.EMP_ID_RECIBE%TYPE,
    P_OBSERVACIONES          IN RECEPCION_MATERIAL.OBSERVACIONES%TYPE,
    P_RECEPCION_MATERIAL_ID  OUT RECEPCION_MATERIAL.RECEPCION_MATERIAL_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO RECEPCION_MATERIAL (
      ORDEN_COMPRA_ID, FECHA_RECEPCION, EMP_ID_RECIBE, OBSERVACIONES, CREATED_AT, ESTADO
    ) VALUES (
      P_ORDEN_COMPRA_ID, P_FECHA_RECEPCION, P_EMP_ID_RECIBE, P_OBSERVACIONES, SYSTIMESTAMP, 'PENDIENTE'
    )
    RETURNING RECEPCION_MATERIAL_ID INTO P_RECEPCION_MATERIAL_ID;
  END SP_INSERTAR_RECEPCION_MATERIAL;

  PROCEDURE SP_ACTUALIZAR_RECEPCION_MATERIAL(
    P_RECEPCION_MATERIAL_ID  IN RECEPCION_MATERIAL.RECEPCION_MATERIAL_ID%TYPE,
    P_ORDEN_COMPRA_ID        IN RECEPCION_MATERIAL.ORDEN_COMPRA_ID%TYPE,
    P_FECHA_RECEPCION        IN RECEPCION_MATERIAL.FECHA_RECEPCION%TYPE,
    P_EMP_ID_RECIBE          IN RECEPCION_MATERIAL.EMP_ID_RECIBE%TYPE,
    P_OBSERVACIONES          IN RECEPCION_MATERIAL.OBSERVACIONES%TYPE
  ) AS
  BEGIN
    UPDATE RECEPCION_MATERIAL
    SET ORDEN_COMPRA_ID = P_ORDEN_COMPRA_ID,
        FECHA_RECEPCION = P_FECHA_RECEPCION,
        EMP_ID_RECIBE   = P_EMP_ID_RECIBE,
        OBSERVACIONES   = P_OBSERVACIONES,
        UPDATED_AT      = SYSTIMESTAMP
    WHERE RECEPCION_MATERIAL_ID = P_RECEPCION_MATERIAL_ID;
  END SP_ACTUALIZAR_RECEPCION_MATERIAL;

  PROCEDURE SP_ELIMINAR_RECEPCION_MATERIAL(
    P_RECEPCION_MATERIAL_ID IN RECEPCION_MATERIAL.RECEPCION_MATERIAL_ID%TYPE
  ) AS
  BEGIN
    UPDATE RECEPCION_MATERIAL
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE RECEPCION_MATERIAL_ID = P_RECEPCION_MATERIAL_ID;
  END SP_ELIMINAR_RECEPCION_MATERIAL;

  PROCEDURE SP_OBTENER_RECEPCION_MATERIAL(
    P_RECEPCION_MATERIAL_ID IN  RECEPCION_MATERIAL.RECEPCION_MATERIAL_ID%TYPE,
    P_CURSOR                OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT RM.RECEPCION_MATERIAL_ID, RM.ORDEN_COMPRA_ID, OC.NUM_OC,
             RM.FECHA_RECEPCION, RM.EMP_ID_RECIBE,
             E.NOMBRES || ' ' || E.APELLIDOS AS EMPLEADO_RECIBE,
             RM.OBSERVACIONES, RM.ESTADO
      FROM RECEPCION_MATERIAL RM
      JOIN ORDEN_COMPRA OC ON OC.ORDEN_COMPRA_ID = RM.ORDEN_COMPRA_ID
      LEFT JOIN EMPLEADO E ON E.EMP_ID = RM.EMP_ID_RECIBE
      WHERE RM.RECEPCION_MATERIAL_ID = P_RECEPCION_MATERIAL_ID;
  END SP_OBTENER_RECEPCION_MATERIAL;

  PROCEDURE SP_LISTAR_RECEPCIONES_MATERIAL(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT RM.RECEPCION_MATERIAL_ID, OC.NUM_OC, RM.FECHA_RECEPCION,
             E.NOMBRES || ' ' || E.APELLIDOS AS EMPLEADO_RECIBE,
             RM.ESTADO
      FROM RECEPCION_MATERIAL RM
      JOIN ORDEN_COMPRA OC ON OC.ORDEN_COMPRA_ID = RM.ORDEN_COMPRA_ID
      LEFT JOIN EMPLEADO E ON E.EMP_ID = RM.EMP_ID_RECIBE
      WHERE RM.ESTADO IN ('ACTIVO','PENDIENTE','RECIBIDO')
      ORDER BY RM.FECHA_RECEPCION DESC;
  END SP_LISTAR_RECEPCIONES_MATERIAL;

  PROCEDURE SP_BUSCAR_RECEPCIONES_MATERIAL(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT RM.RECEPCION_MATERIAL_ID, OC.NUM_OC, RM.FECHA_RECEPCION,
             E.NOMBRES || ' ' || E.APELLIDOS AS EMPLEADO_RECIBE,
             RM.ESTADO
      FROM RECEPCION_MATERIAL RM
      JOIN ORDEN_COMPRA OC ON OC.ORDEN_COMPRA_ID = RM.ORDEN_COMPRA_ID
      LEFT JOIN EMPLEADO E ON E.EMP_ID = RM.EMP_ID_RECIBE
      WHERE RM.ESTADO IN ('ACTIVO','PENDIENTE','RECIBIDO')
        AND (
          (UPPER(P_CRITERIO) = 'NUMOC'    AND UPPER(OC.NUM_OC) LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'EMPLEADO' AND UPPER(E.NOMBRES || ' ' || E.APELLIDOS) LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'ESTADO'   AND UPPER(RM.ESTADO) LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY RM.FECHA_RECEPCION DESC;
  END SP_BUSCAR_RECEPCIONES_MATERIAL;

END PKG_RECEPCION_MATERIAL;
