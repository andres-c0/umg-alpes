CREATE OR REPLACE PACKAGE PKG_ABASTECIMIENTO AS

  PROCEDURE SP_INSERTAR_ABASTECIMIENTO(
    P_MP_ID                IN ABASTECIMIENTO.MP_ID%TYPE,
    P_CANTIDAD_SUGERIDA    IN ABASTECIMIENTO.CANTIDAD_SUGERIDA%TYPE,
    P_MOTIVO               IN ABASTECIMIENTO.MOTIVO%TYPE,
    P_ABASTECIMIENTO_ID    OUT ABASTECIMIENTO.ABASTECIMIENTO_ID%TYPE
  );

  PROCEDURE SP_ACTUALIZAR_ABASTECIMIENTO(
    P_ABASTECIMIENTO_ID    IN ABASTECIMIENTO.ABASTECIMIENTO_ID%TYPE,
    P_MP_ID                IN ABASTECIMIENTO.MP_ID%TYPE,
    P_CANTIDAD_SUGERIDA    IN ABASTECIMIENTO.CANTIDAD_SUGERIDA%TYPE,
    P_MOTIVO               IN ABASTECIMIENTO.MOTIVO%TYPE
  );

  PROCEDURE SP_ELIMINAR_ABASTECIMIENTO(
    P_ABASTECIMIENTO_ID IN ABASTECIMIENTO.ABASTECIMIENTO_ID%TYPE
  );

  PROCEDURE SP_OBTENER_ABASTECIMIENTO(
    P_ABASTECIMIENTO_ID IN  ABASTECIMIENTO.ABASTECIMIENTO_ID%TYPE,
    P_CURSOR            OUT SYS_REFCURSOR
  );

  PROCEDURE SP_LISTAR_ABASTECIMIENTOS(
    P_CURSOR OUT SYS_REFCURSOR
  );

  PROCEDURE SP_BUSCAR_ABASTECIMIENTOS(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  );

END PKG_ABASTECIMIENTO;
/

CREATE OR REPLACE PACKAGE BODY PKG_ABASTECIMIENTO AS

  PROCEDURE SP_INSERTAR_ABASTECIMIENTO(
    P_MP_ID                IN ABASTECIMIENTO.MP_ID%TYPE,
    P_CANTIDAD_SUGERIDA    IN ABASTECIMIENTO.CANTIDAD_SUGERIDA%TYPE,
    P_MOTIVO               IN ABASTECIMIENTO.MOTIVO%TYPE,
    P_ABASTECIMIENTO_ID    OUT ABASTECIMIENTO.ABASTECIMIENTO_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO ABASTECIMIENTO (MP_ID, CANTIDAD_SUGERIDA, MOTIVO, CREATED_AT, ESTADO)
    VALUES (P_MP_ID, P_CANTIDAD_SUGERIDA, P_MOTIVO, SYSTIMESTAMP, 'ACTIVO')
    RETURNING ABASTECIMIENTO_ID INTO P_ABASTECIMIENTO_ID;
  END SP_INSERTAR_ABASTECIMIENTO;

  PROCEDURE SP_ACTUALIZAR_ABASTECIMIENTO(
    P_ABASTECIMIENTO_ID    IN ABASTECIMIENTO.ABASTECIMIENTO_ID%TYPE,
    P_MP_ID                IN ABASTECIMIENTO.MP_ID%TYPE,
    P_CANTIDAD_SUGERIDA    IN ABASTECIMIENTO.CANTIDAD_SUGERIDA%TYPE,
    P_MOTIVO               IN ABASTECIMIENTO.MOTIVO%TYPE
  ) AS
  BEGIN
    UPDATE ABASTECIMIENTO
    SET MP_ID             = P_MP_ID,
        CANTIDAD_SUGERIDA = P_CANTIDAD_SUGERIDA,
        MOTIVO            = P_MOTIVO,
        UPDATED_AT        = SYSTIMESTAMP
    WHERE ABASTECIMIENTO_ID = P_ABASTECIMIENTO_ID;
  END SP_ACTUALIZAR_ABASTECIMIENTO;

  PROCEDURE SP_ELIMINAR_ABASTECIMIENTO(
    P_ABASTECIMIENTO_ID IN ABASTECIMIENTO.ABASTECIMIENTO_ID%TYPE
  ) AS
  BEGIN
    UPDATE ABASTECIMIENTO
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE ABASTECIMIENTO_ID = P_ABASTECIMIENTO_ID;
  END SP_ELIMINAR_ABASTECIMIENTO;

  PROCEDURE SP_OBTENER_ABASTECIMIENTO(
    P_ABASTECIMIENTO_ID IN  ABASTECIMIENTO.ABASTECIMIENTO_ID%TYPE,
    P_CURSOR            OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT A.ABASTECIMIENTO_ID, A.MP_ID, MP.NOMBRE AS MATERIA_PRIMA_NOMBRE,
             A.CANTIDAD_SUGERIDA, A.MOTIVO, A.ESTADO
      FROM ABASTECIMIENTO A
      JOIN MATERIA_PRIMA MP ON MP.MP_ID = A.MP_ID
      WHERE A.ABASTECIMIENTO_ID = P_ABASTECIMIENTO_ID;
  END SP_OBTENER_ABASTECIMIENTO;

  PROCEDURE SP_LISTAR_ABASTECIMIENTOS(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT A.ABASTECIMIENTO_ID, MP.NOMBRE AS MATERIA_PRIMA_NOMBRE,
             A.CANTIDAD_SUGERIDA, A.MOTIVO, A.ESTADO
      FROM ABASTECIMIENTO A
      JOIN MATERIA_PRIMA MP ON MP.MP_ID = A.MP_ID
      WHERE A.ESTADO = 'ACTIVO'
      ORDER BY MP.NOMBRE;
  END SP_LISTAR_ABASTECIMIENTOS;

  PROCEDURE SP_BUSCAR_ABASTECIMIENTOS(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT A.ABASTECIMIENTO_ID, MP.NOMBRE AS MATERIA_PRIMA_NOMBRE,
             A.CANTIDAD_SUGERIDA, A.MOTIVO, A.ESTADO
      FROM ABASTECIMIENTO A
      JOIN MATERIA_PRIMA MP ON MP.MP_ID = A.MP_ID
      WHERE A.ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'MATERIAPRIMA' AND UPPER(MP.NOMBRE) LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'MOTIVO'       AND UPPER(A.MOTIVO)  LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY MP.NOMBRE;
  END SP_BUSCAR_ABASTECIMIENTOS;

END PKG_ABASTECIMIENTO;
