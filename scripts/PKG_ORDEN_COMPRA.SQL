CREATE OR REPLACE PACKAGE BODY PKG_ORDEN_COMPRA AS

  PROCEDURE SP_INSERTAR_ORDEN_COMPRA(
    P_NUM_OC            IN ORDEN_COMPRA.NUM_OC%TYPE,
    P_PROV_ID           IN ORDEN_COMPRA.PROV_ID%TYPE,
    P_ESTADO_OC_ID      IN ORDEN_COMPRA.ESTADO_OC_ID%TYPE,
    P_CONDICION_PAGO_ID IN ORDEN_COMPRA.CONDICION_PAGO_ID%TYPE,
    P_FECHA_OC          IN ORDEN_COMPRA.FECHA_OC%TYPE,
    P_SUBTOTAL          IN ORDEN_COMPRA.SUBTOTAL%TYPE,
    P_IMPUESTO          IN ORDEN_COMPRA.IMPUESTO%TYPE,
    P_TOTAL             IN ORDEN_COMPRA.TOTAL%TYPE,
    P_OBSERVACIONES     IN ORDEN_COMPRA.OBSERVACIONES%TYPE,
    P_ORDEN_COMPRA_ID   OUT ORDEN_COMPRA.ORDEN_COMPRA_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO ORDEN_COMPRA (
      NUM_OC, PROV_ID, ESTADO_OC_ID, CONDICION_PAGO_ID,
      FECHA_OC, SUBTOTAL, IMPUESTO, TOTAL, OBSERVACIONES, CREATED_AT, ESTADO
    ) VALUES (
      P_NUM_OC, P_PROV_ID, P_ESTADO_OC_ID, P_CONDICION_PAGO_ID,
      P_FECHA_OC, P_SUBTOTAL, P_IMPUESTO, P_TOTAL, P_OBSERVACIONES, SYSTIMESTAMP, 'ACTIVO'
    )
    RETURNING ORDEN_COMPRA_ID INTO P_ORDEN_COMPRA_ID;
  END SP_INSERTAR_ORDEN_COMPRA;

  PROCEDURE SP_ACTUALIZAR_ORDEN_COMPRA(
    P_ORDEN_COMPRA_ID   IN ORDEN_COMPRA.ORDEN_COMPRA_ID%TYPE,
    P_NUM_OC            IN ORDEN_COMPRA.NUM_OC%TYPE,
    P_PROV_ID           IN ORDEN_COMPRA.PROV_ID%TYPE,
    P_ESTADO_OC_ID      IN ORDEN_COMPRA.ESTADO_OC_ID%TYPE,
    P_CONDICION_PAGO_ID IN ORDEN_COMPRA.CONDICION_PAGO_ID%TYPE,
    P_FECHA_OC          IN ORDEN_COMPRA.FECHA_OC%TYPE,
    P_SUBTOTAL          IN ORDEN_COMPRA.SUBTOTAL%TYPE,
    P_IMPUESTO          IN ORDEN_COMPRA.IMPUESTO%TYPE,
    P_TOTAL             IN ORDEN_COMPRA.TOTAL%TYPE,
    P_OBSERVACIONES     IN ORDEN_COMPRA.OBSERVACIONES%TYPE
  ) AS
  BEGIN
    UPDATE ORDEN_COMPRA
    SET NUM_OC            = P_NUM_OC,
        PROV_ID           = P_PROV_ID,
        ESTADO_OC_ID      = P_ESTADO_OC_ID,
        CONDICION_PAGO_ID = P_CONDICION_PAGO_ID,
        FECHA_OC          = P_FECHA_OC,
        SUBTOTAL          = P_SUBTOTAL,
        IMPUESTO          = P_IMPUESTO,
        TOTAL             = P_TOTAL,
        OBSERVACIONES     = P_OBSERVACIONES,
        UPDATED_AT        = SYSTIMESTAMP
    WHERE ORDEN_COMPRA_ID = P_ORDEN_COMPRA_ID;
  END SP_ACTUALIZAR_ORDEN_COMPRA;

  PROCEDURE SP_ELIMINAR_ORDEN_COMPRA(
    P_ORDEN_COMPRA_ID IN ORDEN_COMPRA.ORDEN_COMPRA_ID%TYPE
  ) AS
    V_EXISTE NUMBER;
  BEGIN
    SELECT COUNT(*) INTO V_EXISTE
    FROM ORDEN_COMPRA_DETALLE
    WHERE ORDEN_COMPRA_ID = P_ORDEN_COMPRA_ID;

    IF V_EXISTE > 0 THEN
      RAISE_APPLICATION_ERROR(-20001, 'No se puede eliminar la orden de compra porque ya tiene detalle registrado.');
    END IF;

    UPDATE ORDEN_COMPRA
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE ORDEN_COMPRA_ID = P_ORDEN_COMPRA_ID;
  END SP_ELIMINAR_ORDEN_COMPRA;

  PROCEDURE SP_OBTENER_ORDEN_COMPRA(
    P_ORDEN_COMPRA_ID IN  ORDEN_COMPRA.ORDEN_COMPRA_ID%TYPE,
    P_CURSOR          OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT OC.ORDEN_COMPRA_ID, OC.NUM_OC, OC.PROV_ID, P.RAZON_SOCIAL,
             OC.ESTADO_OC_ID, E.CODIGO AS ESTADO_OC_CODIGO,
             OC.CONDICION_PAGO_ID, CP.NOMBRE AS CONDICION_PAGO_NOMBRE,
             OC.FECHA_OC, OC.SUBTOTAL, OC.IMPUESTO, OC.TOTAL,
             OC.OBSERVACIONES, OC.ESTADO
      FROM ORDEN_COMPRA OC
      JOIN PROVEEDOR P          ON P.PROV_ID           = OC.PROV_ID
      JOIN ESTADO_ORDEN_COMPRA E ON E.ESTADO_OC_ID      = OC.ESTADO_OC_ID
      JOIN CONDICION_PAGO CP    ON CP.CONDICION_PAGO_ID = OC.CONDICION_PAGO_ID
      WHERE OC.ORDEN_COMPRA_ID = P_ORDEN_COMPRA_ID;
  END SP_OBTENER_ORDEN_COMPRA;

  PROCEDURE SP_LISTAR_ORDENES_COMPRA(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT OC.ORDEN_COMPRA_ID, OC.NUM_OC, P.RAZON_SOCIAL,
             E.CODIGO AS ESTADO_OC, OC.FECHA_OC, OC.TOTAL, OC.ESTADO
      FROM ORDEN_COMPRA OC
      JOIN PROVEEDOR P          ON P.PROV_ID      = OC.PROV_ID
      JOIN ESTADO_ORDEN_COMPRA E ON E.ESTADO_OC_ID = OC.ESTADO_OC_ID
      WHERE OC.ESTADO = 'ACTIVO'
      ORDER BY OC.FECHA_OC DESC;
  END SP_LISTAR_ORDENES_COMPRA;

  PROCEDURE SP_BUSCAR_ORDENES_COMPRA(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT OC.ORDEN_COMPRA_ID, OC.NUM_OC, P.RAZON_SOCIAL,
             E.CODIGO AS ESTADO_OC, OC.FECHA_OC, OC.TOTAL, OC.ESTADO
      FROM ORDEN_COMPRA OC
      JOIN PROVEEDOR P          ON P.PROV_ID      = OC.PROV_ID
      JOIN ESTADO_ORDEN_COMPRA E ON E.ESTADO_OC_ID = OC.ESTADO_OC_ID
      WHERE OC.ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'NUMOC'       AND UPPER(OC.NUM_OC)      LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'PROVEEDOR'   AND UPPER(P.RAZON_SOCIAL) LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'ESTADOOC'    AND UPPER(E.CODIGO)       LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY OC.FECHA_OC DESC;
  END SP_BUSCAR_ORDENES_COMPRA;

END PKG_ORDEN_COMPRA;
