/

CREATE OR REPLACE PACKAGE BODY PKG_CONDICION_PAGO AS

  PROCEDURE SP_INSERTAR_CONDICION_PAGO(
    P_NOMBRE            IN CONDICION_PAGO.NOMBRE%TYPE,
    P_DIAS_CREDITO      IN CONDICION_PAGO.DIAS_CREDITO%TYPE,
    P_DESCRIPCION       IN CONDICION_PAGO.DESCRIPCION%TYPE,
    P_CONDICION_PAGO_ID OUT CONDICION_PAGO.CONDICION_PAGO_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO CONDICION_PAGO (NOMBRE, DIAS_CREDITO, DESCRIPCION, CREATED_AT, ESTADO)
    VALUES (P_NOMBRE, P_DIAS_CREDITO, P_DESCRIPCION, SYSTIMESTAMP, 'ACTIVO')
    RETURNING CONDICION_PAGO_ID INTO P_CONDICION_PAGO_ID;
  END SP_INSERTAR_CONDICION_PAGO;

  PROCEDURE SP_ACTUALIZAR_CONDICION_PAGO(
    P_CONDICION_PAGO_ID IN CONDICION_PAGO.CONDICION_PAGO_ID%TYPE,
    P_NOMBRE            IN CONDICION_PAGO.NOMBRE%TYPE,
    P_DIAS_CREDITO      IN CONDICION_PAGO.DIAS_CREDITO%TYPE,
    P_DESCRIPCION       IN CONDICION_PAGO.DESCRIPCION%TYPE
  ) AS
  BEGIN
    UPDATE CONDICION_PAGO
    SET NOMBRE       = P_NOMBRE,
        DIAS_CREDITO = P_DIAS_CREDITO,
        DESCRIPCION  = P_DESCRIPCION,
        UPDATED_AT   = SYSTIMESTAMP
    WHERE CONDICION_PAGO_ID = P_CONDICION_PAGO_ID;
  END SP_ACTUALIZAR_CONDICION_PAGO;

  PROCEDURE SP_ELIMINAR_CONDICION_PAGO(
    P_CONDICION_PAGO_ID IN CONDICION_PAGO.CONDICION_PAGO_ID%TYPE
  ) AS
    V_EXISTE NUMBER;
  BEGIN
    SELECT COUNT(*) INTO V_EXISTE
    FROM ORDEN_COMPRA
    WHERE CONDICION_PAGO_ID = P_CONDICION_PAGO_ID;

    IF V_EXISTE > 0 THEN
      RAISE_APPLICATION_ERROR(-20001, 'No se puede eliminar la condicion de pago porque esta asignada a ordenes de compra.');
    END IF;

    UPDATE CONDICION_PAGO
    SET ESTADO = 'INACTIVO', UPDATED_AT = SYSTIMESTAMP
    WHERE CONDICION_PAGO_ID = P_CONDICION_PAGO_ID;
  END SP_ELIMINAR_CONDICION_PAGO;

  PROCEDURE SP_OBTENER_CONDICION_PAGO(
    P_CONDICION_PAGO_ID IN  CONDICION_PAGO.CONDICION_PAGO_ID%TYPE,
    P_CURSOR            OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CONDICION_PAGO_ID, NOMBRE, DIAS_CREDITO, DESCRIPCION, ESTADO
      FROM CONDICION_PAGO
      WHERE CONDICION_PAGO_ID = P_CONDICION_PAGO_ID;
  END SP_OBTENER_CONDICION_PAGO;

  PROCEDURE SP_LISTAR_CONDICIONES_PAGO(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CONDICION_PAGO_ID, NOMBRE, DIAS_CREDITO, DESCRIPCION, ESTADO
      FROM CONDICION_PAGO
      WHERE ESTADO = 'ACTIVO'
      ORDER BY NOMBRE;
  END SP_LISTAR_CONDICIONES_PAGO;

  PROCEDURE SP_BUSCAR_CONDICIONES_PAGO(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT CONDICION_PAGO_ID, NOMBRE, DIAS_CREDITO, DESCRIPCION, ESTADO
      FROM CONDICION_PAGO
      WHERE ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'NOMBRE'      AND UPPER(NOMBRE)      LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'DESCRIPCION' AND UPPER(DESCRIPCION) LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY NOMBRE;
  END SP_BUSCAR_CONDICIONES_PAGO;

END PKG_CONDICION_PAGO;
