CREATE OR REPLACE PACKAGE BODY PKG_PROVEEDOR AS

  PROCEDURE SP_INSERTAR_PROVEEDOR(
    P_RAZON_SOCIAL   IN PROVEEDOR.RAZON_SOCIAL%TYPE,
    P_NIT            IN PROVEEDOR.NIT%TYPE,
    P_EMAIL          IN PROVEEDOR.EMAIL%TYPE,
    P_TELEFONO       IN PROVEEDOR.TELEFONO%TYPE,
    P_DIRECCION      IN PROVEEDOR.DIRECCION%TYPE,
    P_CIUDAD         IN PROVEEDOR.CIUDAD%TYPE,
    P_PAIS           IN PROVEEDOR.PAIS%TYPE,
    P_PROV_ID        OUT PROVEEDOR.PROV_ID%TYPE
  ) AS
  BEGIN
    INSERT INTO PROVEEDOR (
      RAZON_SOCIAL, NIT, EMAIL, TELEFONO, DIRECCION, CIUDAD, PAIS, CREATED_AT, ESTADO
    ) VALUES (
      P_RAZON_SOCIAL, P_NIT, P_EMAIL, P_TELEFONO, P_DIRECCION, P_CIUDAD, P_PAIS,
      SYSTIMESTAMP, 'ACTIVO'
    )
    RETURNING PROV_ID INTO P_PROV_ID;
  END SP_INSERTAR_PROVEEDOR;

  PROCEDURE SP_ACTUALIZAR_PROVEEDOR(
    P_PROV_ID        IN PROVEEDOR.PROV_ID%TYPE,
    P_RAZON_SOCIAL   IN PROVEEDOR.RAZON_SOCIAL%TYPE,
    P_NIT            IN PROVEEDOR.NIT%TYPE,
    P_EMAIL          IN PROVEEDOR.EMAIL%TYPE,
    P_TELEFONO       IN PROVEEDOR.TELEFONO%TYPE,
    P_DIRECCION      IN PROVEEDOR.DIRECCION%TYPE,
    P_CIUDAD         IN PROVEEDOR.CIUDAD%TYPE,
    P_PAIS           IN PROVEEDOR.PAIS%TYPE
  ) AS
  BEGIN
    UPDATE PROVEEDOR
    SET RAZON_SOCIAL = P_RAZON_SOCIAL,
        NIT          = P_NIT,
        EMAIL        = P_EMAIL,
        TELEFONO     = P_TELEFONO,
        DIRECCION    = P_DIRECCION,
        CIUDAD       = P_CIUDAD,
        PAIS         = P_PAIS,
        UPDATED_AT   = SYSTIMESTAMP
    WHERE PROV_ID = P_PROV_ID;
  END SP_ACTUALIZAR_PROVEEDOR;

  PROCEDURE SP_ELIMINAR_PROVEEDOR(
    P_PROV_ID IN PROVEEDOR.PROV_ID%TYPE
  ) AS
    V_EXISTE NUMBER;
  BEGIN
    SELECT COUNT(*) INTO V_EXISTE
    FROM ORDEN_COMPRA
    WHERE PROV_ID = P_PROV_ID;

    IF V_EXISTE > 0 THEN
      RAISE_APPLICATION_ERROR(-20001, 'No se puede eliminar el proveedor porque ya tiene ordenes de compra registradas.');
    END IF;

    UPDATE PROVEEDOR
    SET ESTADO     = 'INACTIVO',
        UPDATED_AT = SYSTIMESTAMP
    WHERE PROV_ID = P_PROV_ID;
  END SP_ELIMINAR_PROVEEDOR;

  PROCEDURE SP_OBTENER_PROVEEDOR(
    P_PROV_ID  IN  PROVEEDOR.PROV_ID%TYPE,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT PROV_ID, RAZON_SOCIAL, NIT, EMAIL, TELEFONO, DIRECCION, CIUDAD, PAIS, ESTADO
      FROM PROVEEDOR
      WHERE PROV_ID = P_PROV_ID;
  END SP_OBTENER_PROVEEDOR;

  PROCEDURE SP_LISTAR_PROVEEDORES(
    P_CURSOR OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT PROV_ID, RAZON_SOCIAL, NIT, EMAIL, CIUDAD, ESTADO
      FROM PROVEEDOR
      WHERE ESTADO = 'ACTIVO'
      ORDER BY RAZON_SOCIAL;
  END SP_LISTAR_PROVEEDORES;

  PROCEDURE SP_BUSCAR_PROVEEDORES(
    P_CRITERIO IN VARCHAR2,
    P_VALOR    IN VARCHAR2,
    P_CURSOR   OUT SYS_REFCURSOR
  ) AS
  BEGIN
    OPEN P_CURSOR FOR
      SELECT PROV_ID, RAZON_SOCIAL, NIT, EMAIL, CIUDAD, ESTADO
      FROM PROVEEDOR
      WHERE ESTADO = 'ACTIVO'
        AND (
          (UPPER(P_CRITERIO) = 'NIT'         AND UPPER(NIT)          LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'RAZONSOCIAL' AND UPPER(RAZON_SOCIAL) LIKE '%' || UPPER(P_VALOR) || '%')
          OR
          (UPPER(P_CRITERIO) = 'EMAIL'       AND UPPER(EMAIL)        LIKE '%' || UPPER(P_VALOR) || '%')
        )
      ORDER BY RAZON_SOCIAL;
  END SP_BUSCAR_PROVEEDORES;

END PKG_PROVEEDOR;
